package eai.devass.sinistreat.appli.restClient;

public class AyantsDroit {

    private String LienParente = "";
    private String AdNom = "";
    private String DateNcAd = "";
    private String AdCin = "";

    public String getLienParente() {
        return LienParente;
    }

    public void setLienParente(String lienParente) {
        LienParente = lienParente;
    }

    public String getAdNom() {
        return AdNom;
    }

    public void setAdNom(String adNom) {
        AdNom = adNom;
    }

    public String getDateNcAd() {
        return DateNcAd;
    }

    public void setDateNcAd(String dateNcAd) {
        DateNcAd = dateNcAd;
    }

    public String getAdCin() {
        return AdCin;
    }

    public void setAdCin(String adCin) {
        AdCin = adCin;
    }
}
